# Write 3 functions to calculate the bitwise AND, bitwise OR, and bitwise XOR of two numbers

# bitwise AND
def bitwise_and(n1, n2):
    return n1 & n2

# bitwise OR
def bitwise_or(n1, n2):
    return n1 | n2

# bitwise XOR
def bitwise_xor(n1, n2):
    return n1 ^ n2

bitwise_and(7, 12)
bitwise_or(7, 12)
bitwise_xor(7, 12)